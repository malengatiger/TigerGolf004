package com.aftarobot.aftarobotmigrator.newdata;

import java.io.Serializable;

/**
 *
 * @author admin
 */
public class RoutePointsDTO implements Serializable{

    private String routePointID;
    private double latitude;
    private double longitude;
    private String status, name;
    private String routeID;

    public String getRoutePointID() {
        return routePointID;
    }

    public void setRoutePointID(String routePointID) {
        this.routePointID = routePointID;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRouteID() {
        return routeID;
    }

    public void setRouteID(String routeID) {
        this.routeID = routeID;
    }

}
